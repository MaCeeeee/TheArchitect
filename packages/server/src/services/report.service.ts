// ─── Inventory Report ────────────────────────────────────

async function renderInventoryReport(doc: PDFKit.PDFDocument, project: { name: string }, projectId: string) {
  const subtitle = `Generated ${new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' })}`;
  drawHeader(doc, `Architecture Inventory — ${project.name}`, subtitle);
  drawFooter(doc, 1);

  // Fetch elements from Neo4j
  const records = await runCypher(
    `MATCH (e:ArchitectureElement {projectId: $projectId})
     RETURN e.id as id, e.name as name, e.type as type, e.layer as layer,
            e.status as status, e.riskLevel as riskLevel,
            e.maturityLevel as maturity, e.togafDomain as domain
     ORDER BY e.layer, e.riskLevel DESC`,
    { projectId }
  );

  interface ElementRow {
    name: string;
    type: string;
    layer: string;
    status: string;
    riskLevel: string;
    maturity: number;
    domain: string;
  }

  const elements: ElementRow[] = records.map((r) => ({
    name: r.get('name') || '-',
    type: r.get('type') || '-',
    layer: r.get('layer') || '-',
    status: r.get('status') || '-',
    riskLevel: r.get('riskLevel') || 'low',
    maturity: r.get('maturity')?.toNumber?.() || r.get('maturity') || 3,
    domain: r.get('domain') || '-',
  }));

  if (elements.length === 0) {
    doc.font('Helvetica').fontSize(10).fillColor(DARK).text('No architecture elements found. Import elements to generate an inventory.', MARGIN, doc.y);
    return;
  }

  // Summary metrics
  drawSectionTitle(doc, 'Summary');
  const layers = ['strategy', 'business', 'information', 'application', 'technology'];
  const layerCounts = layers.map((l) => ({ label: l, value: elements.filter((e) => e.layer === l).length }));
  const avgMaturity = elements.reduce((s, e) => s + e.maturity, 0) / elements.length;
  const riskCounts = { critical: 0, high: 0, medium: 0, low: 0 };
  for (const e of elements) {
    const key = e.riskLevel as keyof typeof riskCounts;
    if (key in riskCounts) riskCounts[key]++;
  }

  const summaryY = doc.y;
  const cardW3 = (CONTENT_W - 20) / 3;
  drawMetricCard(doc, MARGIN, summaryY, cardW3, 'Total Elements', `${elements.length}`);
  drawMetricCard(doc, MARGIN + cardW3 + 10, summaryY, cardW3, 'Avg Maturity', avgMaturity.toFixed(1));
  drawMetricCard(doc, MARGIN + (cardW3 + 10) * 2, summaryY, cardW3, 'Critical/High Risk', `${riskCounts.critical + riskCounts.high}`, '#ef4444');
  doc.y = summaryY + 70;

  // Elements by Layer chart
  drawSectionTitle(doc, 'Elements by Layer');
  drawBarChart(doc, MARGIN, doc.y, CONTENT_W, layerCounts.filter((l) => l.value > 0).map((l) => ({
    label: l.label.charAt(0).toUpperCase() + l.label.slice(1),
    value: l.value,
    color: ACCENT,
  })));

  // Tables per layer
  for (const layer of layers) {
    const layerElements = elements.filter((e) => e.layer === layer);
    if (layerElements.length === 0) continue;

    checkPageBreak(doc, 60);
    drawSectionTitle(doc, `${layer.charAt(0).toUpperCase() + layer.slice(1)} Layer (${layerElements.length})`);

    const rows = layerElements.map((e) => [
      e.name,
      e.type,
      e.status,
      e.riskLevel,
      `${e.maturity}/5`,
      e.domain,
    ]);
    drawTable(doc, ['Name', 'Type', 'Status', 'Risk', 'Maturity', 'Domain'],
      rows, [130, 95, 75, 60, 55, 80], 'Architecture Inventory');
  }
}

